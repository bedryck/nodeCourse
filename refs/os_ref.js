const os = require('os') // модуль для роботи з операційною системою

console.log(os.platform()) //платформа

console.log(os.arch()) // архітектура

console.log(os.cpus()) // інформація про процесор

console.log(os.freemem()) // вільна памятать

console.log(os.totalmem()) // уся память

console.log(os.homedir()) // корнєва папка

console.log(os.uptime()) // скільки система працює